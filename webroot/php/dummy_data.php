 function generateRandomEmail($num) {
    $domains = ['example.com', 'email.com', 'testmail.com', 'mail.com', 'sample.org'];
    $chars = 'abcdefghijklmnopqrstuvwxyz1234567890';
    
    
    $emails = [];
    
    for ($i = 0; $i < $num; $i++) {    
        // Generate random name for the email (8 characters)
        $emailName = '';
        for ($i = 0; $i < 8; $i++) {
            $emailName .= $chars[rand(0, strlen($chars) - 1)];
        }

        // Add random domain from the list
        $email = $emailName . '@' . $domains[array_rand($domains)];
    
        $emails[] = $email;
    
    }
    
    return $emails;    
}   
 function generateRandomString($num, $inc_char= true, $inc_nr= true) {
    $buildFrom = '';
    $chars = 'abcdefghijklmnopqrstuvwxyz';
    $nrs   = '1234567890';
    if($inc_char){
        $buildFrom .= $chars;
    }
    if($inc_nr){
        $buildFrom .= $nrs;
    }
    
    $string = '';
    
    for ($i = 0; $i < $num; $i++) {
        $string .= $buildFrom[rand(0, strlen($buildFrom) - 1)];
    }
    
    return $string;    
